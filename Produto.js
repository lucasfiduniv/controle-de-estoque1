class Produto{
    nome;
    descricao;
    codigo;
    preco;
    quantidade;
    fornecedor;
    constructor (nome, descricao,codigo,preco,quantidade,fornecedor) {
        this.nome = nome
        this.descricao = descricao
        this.codigo = codigo
        this.preco = preco
        this.quantidade = quantidade
        this.fornecedor = fornecedor
    }
}